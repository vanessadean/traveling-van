					<h4 class="heading">The Nitty Gritty</h4>
					<div class="text">
					<p>We rented a car to get from Lisbon to Porto and back again and the rental process and drive up were both super easy. Of course it helped that my travel mate spoke Spanish fluently, which was close enough to Portuguese for us to easily communicate with the guys at the airport car rental. No one in Lisbon was offended when she used Spanish to communicate, but the lady running our hostel in Porto did not seem to like it. <a href="http://wikitravel.org/en/Porto" target="_blank">Wikitravel</a> sums up the reason behind that better than I can: "If you speak in Spanish to a local, you will be largely understood and as a rule they will freely converse with you, but from time to time, more so with the older generation, you may be politely reminded that you are in Portugal and the native language is Portuguese."</p>
					<p>Our patroness was polite enough about it, even though she ignored my friend and insisted on solely addressing me in Portuguese, but we were able to get by. The hostel itself was pretty charmless, but it was clean, the location was good and our room had a nice big bathroom. You could do worse than <a href="http://www.booking.com/hotel/pt/residencial-triunfo.en.html" target="_blank">Residencial Triunfo</a> in Porto.</p>
