					<h4 class="heading">The Sights</h4>
					<div class="text">
					<p>Porto's biggest tourist draws are the port wine cellars, but it is also one of Europe&#39;s oldest city centers. It&#39;s picturesquely located on rocky hills overlooking the Douro River and most of the port cellars are right across the river in the city of Gaia. The first thing we did when we arrived in Porto was walk across the Ponte Dom Luis and take the <a href="http://www.gaiacablecar.com/Teleferico_UK.html" target="_blank">Telef&eacute;rico de Gaia</a>, a gondola, from the top of the bridge down to the cellars. It&#39;s quick and easy and affords a beautiful view of the Douro river and Porto across the way.</p>

					<p>There are a ton of port cellars that you can visit in Gaia, but we only made it to one at <a href="http://eng.sograpevinhos.eu/visitas/cave/6" target="_blank">Ferreira Cellars</a>. Ferreira was founded by a local Douro family and was piloted to prominence by Dona Ant√≥nia Adelaide Ferreira, one of the few women in the port business at the time and since. I wish I could tell you more about her, but we chose to take the tour in Portuguese because we didn&#39;t want to wait for the English tour. I picked up about half of what our guide was saying, but the tour itself wasn&#39;t as important to us as tasting the wine at the end.</p>
					<p>On the way back to Porto from Gaia we walked along the lower section of the Ponte Dom Luis and took the <a href="http://www.metrodoporto.pt/PageGen.aspx?WMCM_PaginaId=10035" target="_blank">Funicular dos Guindais</a> back up to where our hostel was located, another fun and easy way to get from point A to B.</p>
					
					<p>On our second day in Porto we left the city for a drive out to visit the vineyards along the Douro River. We ended up being the only two people on the tour and our guides from <a href="http://www.portodouro.com/" target="_blank"> Porto Douro Tours</a> were fantastic. Props to our driver for making her way up some pretty scary, tight roads up the hills to our first stop at <a href="http://www.luuux.com/places/caves-da-murganheira" target="_blank">Murganheira Cellars</a>. We made a couple of other stops, but the highlight of the tour for me was walking around the gardens at <a href="http://www.aveledaportugal.pt/" target="_blank">Quinta Aveleda</a> vineyard. We also, of course, tasted plenty of Quinta Aveleda&#39;s speciality, vinho verde. Vinho verde is made with young grapes before they are fully ripened, which makes for a light, refreshing white wine with a lower alcohol content and slight fizz. It&#39;s delicious served very cold and is a great summer wine.</p>
