					<h4 class="heading">Food and Booze</h4>
					<div class="text">
					<p>The most scenic options for dinner in Porto are the restaurants lined up along the Cais de Ribeira facing the Douro River and Gaia. We visited Porto in March so it was pretty chilly, but we still chose to sit outside under the heat lamps and it was delightful. I think I was talked into some type of bacalao dish, which is a salted codfish that you really only need to try once unless you enjoy having your taste buds seared off by salt. I&#39;m ashamed to say that I can&#39;t remember the name of the restaurant we went to, but I&#39;ll give a shout out to these guys for having a fun little website: <a href="http://www.acanastradaribeira.com/" target="blank">Canastra da Ribeira</a>.<p>
					<p>We were only in Porto for two nights and on our second night we had dinner at a tapas and wine bar near our hostel called <a href="https://www.facebook.com/pages/Canelas-de-Coelho/476475225756689" target="_blank">Canelas de Coelho</a>. The decor and the food both had a modern but cosy feel and the desserts were crazy delicious. I recommend it if you&#39;re looking for a spot that the locals might frequent.</p> 
					<p>And last but not least, we could not leave Porto without eating a Francesinha sandwich. This abomination is a bit like a croque monsieur gone wild, or a croque madame if you chose to add a fried egg on top. We ordered the classic Francesinha with a filling of ham, roast beef and two different types of sausage, a slice of melted cheese and a fried egg on top, served on a bed of french fries with gravy poured all over everything. According to our waiter, this sandwich is always, ALWAYS, accompanied by a glass of beer and never water. Even when you order one at 10am, which is something that we may not have done had we known what we were getting into. This is a must though. Do not leave Porto until you eat this sandwich.</p>
