				<p>On our way back from Porto down to Lisbon we decided to stay a night in the Castelo de Obidos, a castle that has been converted to a hotel by <a href="http://www.pousadasofportugal.com/" target="_blank">Pousadas de Portugal</a>. Pousadas de Portugal is a state-owned enterprise that runs scenic hotels, usually in historic buildings like old castles. We picked the Castelo de Obidos because it really looks like a medieval castle. The town of Obidos is totally encompassed by the castle walls, with the castle proper at the top of a large hill. The one challenge we didn&#39;t envision when we booked the hotel was driving our Peugot through a small opening in the castle walls and up a steep hill along the tiniest cobblestone streets that I have ever seen. We managed to do it without destroying the Peugot or any historic buildings, but it was certainly nerve wracking.</p> 

