				<h4 class="heading">The Nitty Gritty</h4>
					<div class="text">
					<p>There are several tour companies that will take you into the outback, and after a little research we decided to go with <a href="http://www.emurun.com.au/" target="_blank">Emu Run Tours</a>. They do a few different types, but we heard from Australian friends that the best way to experience the outback is camping in a swag. I realize now that this is a good example of the Aussie sense of humor, and that they may have been messing with us a bit. A swag is essentially a big canvas sack with a sleeping bag in it, which was roughing it harder than I ever have before. I'm so glad we did it though. It was too hot to be in a tent anyway, and we were all so exhausted at the end of the day that we barely had time to look up at the stars before falling asleep. I&#39;m partially glad that I did it for the bragging rights, but mostly because the three-day tour gave us time to hike around Kata Tjuta and Kings Canyon. You have to see Uluru because that&#39;s the big landmark, but the other two spots were really what I enjoyed most about the trip.</p>

					<p>One last thing. There are a couple of options for getting to Uluru for a tour. We chose to fly into Alice Springs and start our tour there because it worked out with timing and our budget, but you can also fly directly into Ayers Rock instead.</p>
