				<h4 class="heading">Food and Booze</h4>
					<div class="text">
					<p>Our first pit stop as we drove from Alice Springs into the outback was a little town called Curtin Springs that sells &#8220;Fucking Good Port.&#8221; I can&#39;t even remember the story of who first started making the port or why they sell it there, but they are not kidding. It&#39;s delicious.</p>

					<p>We also ate a few interesting things during our visit, including Emu and Kangaroo burgers (a little tough and generally unmemorable) and some tasty damper. Damper is traditionally a simple, unleavened bread that is baked in the dampened hot coals of a campfire. Our tour guide made us a fancy version with blue cheese, and if you&#39;re feeling adventurous here are some <a href="http://www.squidoo.com/Damper" target="_blank">recipes</a> that you can try at home or on your next camping trip.</p>
