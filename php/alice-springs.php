				<h4 class="heading">The Sights</h4>
					<div class="text">
					<p>Uluru (Ayers Rock), is one of Australia&#39;s best known landmarks and a major tourist attraction. We visited during the off-season and it was nice to avoid some of the crowds, but I wouldn&#39;t dare to go back in full-blown summer. We lucked out with, what was described as, mild weather for that time of year, but it was still brutally hot. There were points in the hike around Uluru when I didn&#39;t think I was going to make it.</p>

					<p>The other two major stops on our tour of the Outback were Kata Tjuta (the Olgas) and Kings Canyon. I fared much better during our early morning hikes at these spots; the only time in recent memory that I&#39;ve been up before the sun. Watching the sun rise across the top of Kings Canyon before hiking down into the Garden of Eden was incredible though. We all dove into a watering hole at the bottom of the canyon and floating in the cool water, looking up at the copper canyon walls against the blue sky felt like heaven.</p> 

					<p>The other memorable thing that we did on our trip was feed the rock wallabies at the <a href="http://www.auroraresorts.com.au/Attractions/HandFeedRockWallabiesAliceSprings.aspx" target="_blank">Heavitree Gap Outback Lodge</a> in Alice Springs. The hotel sells bags of food pellets and as the sun sets you just sit at the bottom of a hill and wait for the wallabies to come to you. They literally eat right out of your hand. I even encountered a very hungry mama wallaby with a joey in her pouch that gripped my hand so tightly I could feel her little claws. I&#39;m aware that feeding wild animals may not be an ecologically sound practice, but it was really freaking fun.</p>
