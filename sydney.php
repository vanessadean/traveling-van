					<p>The absolute highlight of our trip to Sydney though was the <a href="http://www.bridgeclimb.com/" target="_blank">Sydney Harbor Bridge Climb</a>. If you do it, splurge for the twilight tour over the top, or the dawn tour if you&#39;re a maniac. The view of the harbor from the top of the bridge is amazing, especially as the sun sets and the lights of the city turn on. Fair warning, if you are afraid of heights think very hard before taking the tour. You are strapped onto the bridge the whole time and it&#39;s perfectly safe, but there is no turning back once you get out there. There is also at least one point of the tour where you can see straight down to the water below.</p> 
