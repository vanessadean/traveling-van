					<p>Accommodations</p>
					<p>We flew to and from the states through Sydney, with visits to other parts of Australia in between, so our time there was split between two different hostels, the Y Hotel Hyde Park and Sydney Harbour YHA.</p>

					<p>Although the <a href="http://www.yhotel.com.au/content/hydepark.htm" target="_blank">Y Hotel Hyde Park</a> was not glamorous, it was clean, quiet and in a perfect location close to Hyde Park, a subway station and the bus to Bondi Beach. They also upgraded us to a private ensuite. Similar results not guaranteed, but I still highly recommend it.</p>

