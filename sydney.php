					<h4 class="heading">The Sights</h4>
					<div class="text">
					<p>There are so many great things about Sydney, but for me the kicker is how easy it is to get from the city center to fantastic public beaches. My friends and I visited Bondi and Manly beaches while we were in town, and although the weather didn&#39;t cooperate with us for sunbathing at Bondi, the beach itself is gorgeous. It was also great weather for a little hike along the coastline up to Bronte.</p> 
					
