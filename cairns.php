				<div id="three">
					<h4 class="heading">The Nitty Gritty</h4>
					<div class="text">
					<p>When we were looking for affordable places to stay near the Great Barrier Reef, Cairns seemed like the best option for hostels. And even though it was obviously the most affordable town, for whatever reason, I still expected it to be a charming little beachside village. Not the case. Cairns serves mostly as a starting point for tourists in the area, rather than a destination. Except for what appeared to be quite a few down-on-their luck backpackers living on the street. If you&#39;re looking for a charming beachside village, pony up and stay in Port Douglas or Palm Cove.</p>

					<p>Cairns grew on me though because we stayed in a fantastic hostel. The <a href="http://www.travellersoasis.com.au/" target="_blank">Traveler&#39;s Oasis</a> was clean, charming and run by some of the nicest and most helpful people that we met the entire trip. They were incredibly patient with the million and one questions that we had about deadly plant and animal life in the area. I also never would have agreed to our self-guided tour of the Atherton Tablelands if they hadn&#39;t encouraged us to explore and helped us plan out our route. On top of that, they set aside pizzas for us on pizza night even though we showed up really late for dinner after our hike. The only drawback is that the hostel is a little on the outskirts of town. Although, I don&#39;t see much reason to be close to the center of town. At any rate, read the reviews before you book anywhere. The horror stories that I read about other hostels made me appreciate how lucky we were to find the place.</p> 
