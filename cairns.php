					<h4 class="heading">Food and Booze</h4>
					<div class="text">
					<p>Our hostel in Cairns was right next to a great bar/restaurant called the <a href="http://www.greenantcantina.com/" target="_blank">Green Ant Cantina</a>. It had delicious food, a fantastic outdoor patio space and its own line of beer, including a boozy ginger beer that I loved.</p>

					<p>Another nice place that we found for lunch was <a href="http://www.vivo.com.au/" target="_blank"> Vivo Bar and Grill</a> in Palm Cove. There isn&#39;t really a swim beach in Cairns, but there are beautiful beaches up the coast between Cairns and Port Douglas and we took a day trip up to Palm Cove. Even though swimming was limited beause of the stingers, the beach was gorgeous and the watermelon martini at Vivo hit the spot.</p>
