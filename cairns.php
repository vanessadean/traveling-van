					<h4 class="heading">The Sights</h4>
					<div class="text">
					<p>It&#39;s horrifying to think that by visiting the Great Barrier Reef you might be contributing to its demise. We did quite a bit of research to find an eco-certified company that did smallish tours to the outer reef and went with <a href="http://www.poseidon-cruises.com.au/" target="_blank">Poseidon Cruises</a> out of Port Douglas for a snorkeling day tour. It was stinger season during our trip and they provided full-body stinger wetsuits for everyone, which were great for keeping warm in the water and had the added bonus of making everyone look like ridiculous James Bond-type villains. Stingers are tiny jellyfish with venom that attacks your nervous system resulting in death or, best case scenario, crushing pain throughout your body that lasts for several days.</p> 

					<p>It was easy to forget about the stingers though because the reef is gorgeous and the fish are spectacular. We made stops at three different reefs on the tour, and I had to drag myself out of the water each time they called us back to the boat. If I ever make it back, I&#39;m going to do a three- or five-day scuba tour, so I can really get face to face with the fishies.</p> 

					<p>The other fun thing that we did while we were in Queensland was a tour of the <a href="http://www.athertontablelands.com.au/" target="_blank">Atherton Tablelands</a>. I was really skeptical that the three of us could just hop into a car and drive into the rainforest, but we didn&#39;t get lost once and it ended up being a great day. My favorite part of the day was swimming under the Millaa Millaa waterfalls. I think we lucked out on our timing because there was hardly anyone else around, even though it&#39;s a major stop for tours in the area. If you really want to avoid the crowds there are a couple of other waterfalls on the circuit, Zillie and Ellinjaa Falls, that aren&#39;t quite as accessible for tour buses.</p> 

					<p>Another great spot for swimming and some easy hiking is Lake Eacham, although we had limited luck spotting any exotic local wildlife. We were hoping to find platypi, but the only creatures we encountered were some turtles, lots of wild turkeys and one little snake.</p> 
